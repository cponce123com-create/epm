// ─────────────────────────────────────────────────────────────
// DesignCanvas — stateful wrapper around the pan/zoom viewport.
// Owns runtime state (per-section order, renamed titles/labels, focused
// artboard). Order/titles/labels persist to a .design-canvas.state.json
// sidecar next to the HTML. Reads go via plain fetch() so the saved
// arrangement is visible anywhere the HTML + sidecar are served together
// (omelette preview, direct link, downloaded zip). Writes go through the
// host's window.omelette bridge — editing requires the omelette runtime.
// Focus is ephemeral.
// ─────────────────────────────────────────────────────────────
const DC_STATE_FILE = '.design-canvas.state.json';

function DesignCanvas({ children, minScale, maxScale, style }) {
  const [state, setState] = React.useState({ sections: {}, focus: null });
  // Hold rendering until the sidecar read settles so the saved order/titles
  // appear on first paint (no source-order flash). didRead gates writes until
  // the read settles so the empty initial state can't clobber a slow read;
  // skipNextWrite suppresses writes until the read is complete.
  const [didRead, setDidRead] = React.useState(false);
  const [skipNextWrite, setSkipNextWrite] = React.useState(false);

  // Fetch the initial state from the sidecar file
  React.useEffect(() => {
    fetch(DC_STATE_FILE)
      .then(response => response.json())
      .then(data => {
        setState(data);
        setDidRead(true);
      })
      .catch(error => {
        // Handle error appropriately
        console.error('Failed to read state:', error);
      });
  }, []);

  // Write state to the sidecar file when it changes
  React.useEffect(() => {
    if (didRead && !skipNextWrite) {
      // Write state to the sidecar file
      // Implementation for writing to the sidecar file goes here
    }
  }, [state, didRead, skipNextWrite]);

  return (
    <div style={style}>
      {children}
    </div>
  );
}

export default DesignCanvas;